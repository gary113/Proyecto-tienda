    private void btnConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConectarActionPerformed
        
        String Usuario=txtUsuario.getText();
        String Contraseña = new String(txtContraseña.getPassword());
        BD ObjBD = new BD(Usuario, Contraseña);
        
        if(ObjBD.Conexion!=null){
            Principal VerPrincipal = new Principal(ObjBD.Conexion);
            VerPrincipal.setLocationRelativeTo(null);
            VerPrincipal.show();
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(null, "Por favor ingrese credenciales válidas", "Error", JOptionPane.ERROR_MESSAGE);
            txtUsuario.setText("");
            txtContraseña.setText("");
            txtUsuario.requestFocus();
        }
        
    }//GEN-LAST:event_btnConectarActionPerformed

